	</section>

	<section aria-labelledby="gameplay-title">
		<h3 id="gameplay-title" class="text-xl mb-2 font-bold">Gameplay</h3>
		<ul class="list-disc ps-4 lg:ps-5">
			<li>
				You start the game with 5 lives and a limited amount of time to guess the rebus puzzle.
			</li>
			<li>
				You have to guess the rebus puzzle one letter at a time using the on-screen or physical
				keyboard.
			</li>
			<li>Correct guesses reveal the first occurrence of the letter in the rebus puzzle.</li>
			<li>Incorrect guesses cost a life and the guessed letter is then disabled.</li>
			<li>If you guess the rebus puzzle before running out of lives or time, you win!</li>
			<li>If you lose all your lives or run out of time, the game ends in a loss.</li>
		</ul>
	</section>

	<section aria-labelledby="leaderboard-title">
		<h3 id="leaderboard-title" class="text-xl mb-2 font-bold">Climbing the Leaderboard</h3>
		<ul class="list-disc ps-4 lg:ps-5">
			<li>
				Your score determines your position on the daily, weekly, and all-time leaderboards. The
				daily leaderboard resets every day, the weekly one resets every week, and the all-time
				leaderboard showcases the top players overall.
			</li>
			<li>
				Scores are calculated after each game based on your remaining lives and time left. Play more
				games to improve your score!
			</li>
		</ul>
	</section>

	<section aria-labelledby="points-title">
		<h3 id="points-title" class="text-xl mb-2 font-bold">Points System</h3>
		<ul class="list-disc ps-4 lg:ps-5">
			<li>+20 points for each remaining life.</li>
			<li>+100 points for winning in Easy difficulty.</li>
			<li>+200 points for winning in Medium difficulty.</li>
			<li>+300 points for winning in Hard difficulty.</li>
			<li>
				100 total points are distributed across all correct guesses. So, if there are 4 characters
				to guess, each correct guess earns 25 points.
			</li>
			<li>
				For time remaining, points are calculated based on the percentage of time left. For example,
				if 100 seconds is the total time and you finish with 60 seconds remaining, you get 60
				points.
			</li>
		</ul>
	</section>

