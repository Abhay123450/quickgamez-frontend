	<section aria-labelledby="start-title">
		<h3 id="start-title" class="text-xl mb-2 font-bold">Starting the Game</h3>
		<ul class="list-disc ps-4 lg:ps-5">
			<li>Choose a difficulty level: Easy, Medium, or Hard.</li>
